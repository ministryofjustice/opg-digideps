<?php declare(strict_types=1);

namespace AppBundle\Entity;


class CourtOrderDeputy
{
    /** @var string */
    private $email;

    /**
     * @return string
     */
    public function getEmail(): string
    {
        return $this->email;
    }

    /**
     * @param string $email
     */
    public function setEmail(string $email): self
    {
        $this->email = $email;

        return $this;
    }
}
